#!/usr/bin/env node
// Build public/.htaccess for the static (IONOS/Apache) deploy.
//
// WHY: nuxt.config routeRules redirects are served by the Nitro Node server, which
// does not exist on IONOS. Without this, every old WordPress URL (482 posts + the
// curated page redirects) would dead-end on the live site — losing the inbound links
// and search equity the migration is meant to preserve. This emits the SAME redirect
// map as real Apache 301s, plus clean-URL serving for the prerendered output.
//
// Single source of truth: app/redirects.json (generated) + app/redirects.manual.mjs.
// Runs automatically before `nuxt generate` (see package.json). Regenerate after
// changing either map.
//
// Host canonicalisation (HTTPS + one true host) is OFF by default so the 2026 staging
// host is left alone; enable at launch with:
//   NUXT_PUBLIC_SITE_URL=https://lasunison.com SEO_FORCE_CANONICAL=true
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'
import { manualRedirects } from '../app/redirects.manual.mjs'

const HERE = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(HERE, '..')
const OUT = resolve(ROOT, 'public/.htaccess')

const postRedirects = JSON.parse(readFileSync(resolve(ROOT, 'app/redirects.json'), 'utf8'))

// Escape regex metacharacters so a literal old path can't be mis-parsed.
const escapeRe = s => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')

const siteUrl = process.env.NUXT_PUBLIC_SITE_URL || 'https://2026.lasunison.com'
const forceCanonical = process.env.SEO_FORCE_CANONICAL === 'true'
const canonicalHost = (() => {
  try {
    return new URL(siteUrl).host
  }
  catch {
    return ''
  }
})()

// Manual page redirects win over the generated post redirects where they overlap
// (same precedence as the routeRules spread order in nuxt.config.ts).
const redirects = { ...postRedirects, ...manualRedirects }

const L = []
L.push('# Generated by scripts/build-htaccess.mjs — DO NOT EDIT BY HAND.')
L.push('# Real 301 redirects + clean-URL serving for the static (IONOS/Apache) deploy.')
L.push('# Regenerate after editing app/redirects.json or app/redirects.manual.mjs.')
L.push('')
L.push('Options +FollowSymLinks')
L.push('DirectorySlash Off')
L.push('RewriteEngine On')
L.push('')

if (forceCanonical && canonicalHost) {
  L.push(`# --- Force HTTPS + canonical host (${canonicalHost}) in a single hop ---`)
  L.push(`RewriteCond %{HTTP_HOST} !=${canonicalHost} [OR]`)
  L.push('RewriteCond %{HTTPS} off')
  L.push(`RewriteRule ^ https://${canonicalHost}%{REQUEST_URI} [R=301,L]`)
  L.push('')
}

L.push(`# --- 301 redirects: old WordPress URLs → new paths (${Object.keys(redirects).length}) ---`)
for (const [from, to] of Object.entries(redirects)) {
  const pattern = '^' + escapeRe(from.replace(/^\//, '')) + '/?$'
  // NE keeps a target's #fragment intact (e.g. /branch#representatives).
  L.push(`RewriteRule ${pattern} ${to} [R=301,L,NE]`)
}
L.push('')

L.push('# --- Serve prerendered HTML for clean, extensionless URLs (no forced trailing slash) ---')
L.push('# nuxt generate writes <route>/index.html; this serves it for /route directly,')
L.push('# with DirectorySlash Off above suppressing mod_dir\'s automatic slash redirect.')
L.push('RewriteCond %{REQUEST_FILENAME} !-f')
L.push('RewriteCond %{REQUEST_FILENAME} !-d')
L.push('RewriteCond %{DOCUMENT_ROOT}/$1/index.html -f')
L.push('RewriteRule ^(.+?)/?$ /$1/index.html [L]')
L.push('')

L.push('# --- 404 ---')
L.push('ErrorDocument 404 /404.html')
L.push('')

writeFileSync(OUT, L.join('\n'))
console.log(`✓ wrote ${OUT}`)
console.log(
  `  ${Object.keys(redirects).length} redirect rules`
  + (forceCanonical && canonicalHost
    ? `; canonical host = ${canonicalHost} (HTTPS forced)`
    : '; no host canonicalisation (staging-safe)'),
)
